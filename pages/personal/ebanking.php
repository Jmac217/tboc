<h2>Online Banking</h2>
<div class='sub_div'>
<p>Online banking gives you access to account balances, loan payments, transaction history, debit and ATM card activitiy, as well as ACH deposits and withdrawls.<sup>1</sup></p>
<p>With online banking, your information is always accurate. Featuring a real-time interface to our database, online banking reflects account activity as it happens.</p>
</div>
<h2>Bill Pay</h2>
<div class='sub_div'>
	<p>Customers can add Basic Bill Pay to online banking, and get web access to pay bills online. Basic Bill Pay includes 15 monthly transactions, with a .35 charge for each additional transaction. Bill pay customers can pay any bill from any checking account inside online banking, and with guaranteed delivery, you can be assured that your bills are paid on time.</p>
</div>
