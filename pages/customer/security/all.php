<br /><br /><b class='bold'>Protect Your Identity</b><br /><br />

<strong>Helpful Tips for Protecting Your Identity:</strong><br /><br />
<ul>
	<li>Shred all documents containing personal information before throwing them away (e.g. bank statements, credit card statements, utility bills, medical information, credit card offers)</li><br />
	<li>Use a locked, secure mailbox or US Post Office Box</li><br />
	<li>Do not provide personal information to individuals in person, on the phone or online unless it is sources you know or have contacted first.</li><br />
	<li>Review your monthly bank statements and other billing statements carefully for any suspicious activity or charges you didn't authorize.</li><br />
	<li>Do not respond to any email, phone or text messages that ask for your personal or financial information.  Don't click on email links in the message, or call phone numbers that are left on your answering machine.</li><br />
	<li>Get a free copy of your credit report by visiting <b>www.annualcreditreport.com</b> or by calling 1-877-322-8228.</li><br />
</ul>	
	This central site allows you to request a free credit report once every 12 months from	each of the nationwide consumer credit reporting agencies: <i>Equifax</i>, <i>Experian</i> and <i>TransUnion</i>.
