<b class='bold'>What Is Identity Theft?</b><br /><br />
<span class='span'>
Identity Theft occurs when someone uses your name, date of birth, Social Security number, or some other personal, financial, or medical information without your permission to commit fraud or some other crime, generally for economic gain.  Identity theft is also used to fraudulently obtain government documents such as driver licenses and birth certificates.  
Victims of identity theft can spend many long hours trying to repair the damage that criminals have done to their credit record.    
</span>
<br /><br /><br />

<b class='bold'>How Do Thieves Get Your Information?</b><br /><br />
<span class='span'>
Thieves get your personal information in a variety of ways so they can steal your identity and your money.<br />  These include in-person, phone, and online methods.  Examples of some of the methods used include: 
	<br /><br />
	<strong class='strong'>Stealing mail from mailboxes</strong><br />
		<div class='div'>Taking bills, credit card applications, bank statements and other correspondence which may have personal information contained on them.</div>
	<strong class='strong'>Dumpster Diving</strong>
		<div class='div'>Looking through the trash for discarded mail, bills, credit card applications, etc to obtain personal information.</div>
	<strong class='strong'>Phishing</strong>
		<div class='div'>An email that appears to be from a legitimate organization sent to a large email list with the hope of luring some users into giving out personal financial information.</div>
	<strong class='strong'>Vishing</strong>
		<div class='div'>Phone call that appears to be coming from a legitimate company asking you to provide personal information.  The call can be from a live person or may be an automated message.</div>
	<strong class='strong'>Smishing <i>(SMS Phishing)</i></strong>
		<div class='div'>Text message on your cell phone asking you to provide personal information or directing you to a web site where you will be asked for personal information.</div>
	<strong class='strong'>Spoof Websites</strong>
		<div class='div'>Fake Websites created to appear as official ones.</div><br />
	<span class='small'>		
		<b>Additional Information from the Federal Trade Commission:</b><br />
		<a href='../outside.php?u=http://www.consumer.ftc.gov/articles/0003-phishing'>Phishing Scams</a><br />
		<a href='../outside.php?u=http://www.consumer.ftc.gov/articles/0076-telemarketing-scams'>Telemarketing Scams</a><br />
	</span>
</span>
<br /><br />
<b class='bold'>Warning Signs That Someone Has Stolen Your Information</b><br /><br />
	<span>> Unauthorized charges on your bank statements and other billing statements you didn't authorize.</span><br /><br />
	<span>> Missing bills in the mail.</span><br /><br />
	<span>> Unexpected bills for purchases you did not make or medical services you did not use.</span><br /><br />
	<span>> Receiving credit cards in the mail that you did not request.</span><br /><br />
	<span>> Calls or letters from debt collectors about purchases you have not made.</span><br /><br />
	<span>> Being denied credit or being offered a high interest rate on a new account.</span><br /><br />
	<span>> The IRS notifies you that more than one tax return was filed in your name.</span><br /><br />
	<span>> Notification of a data breach at a company you have done business with.</span><br /><br />
	<strong class='bold_black'>If you have any of the above warning signs, you need to take immediate action.</strong><br />
